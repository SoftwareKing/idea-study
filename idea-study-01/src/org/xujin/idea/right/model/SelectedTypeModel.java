package org.xujin.idea.right.model;

/**
 * 下拉列表选中模型
 * @author xujin
 */
public class SelectedTypeModel {

    /**
     * 下拉列表显示的名称
     */
    private String name;

    /**
     * 下拉列表的value
     */
    private String value;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }


}
